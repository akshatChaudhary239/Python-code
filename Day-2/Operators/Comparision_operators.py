# Comparision operators (We always get type boolean in comparision operators)
#  == , !=, > , < , >= , <=

# Examples

Num1 = 22
Num2 = 23
print(Num1==Num2) # False

Num3 = 45
Num4 = 55
print(Num4>=Num3) # True

Num5 = 65
Num6 = 65
print(Num5!=Num6) #False

Num7 = 80
